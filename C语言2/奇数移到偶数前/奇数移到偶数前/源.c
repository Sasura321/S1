//1.调整数组使奇数全部都位于偶数前面。输入一个整数数组，实现一个函数,
//来调整该数组中数字的顺序使得数组中所有的奇数位于数组的前半部分，所有偶数位于数组的后半部分。

#include<stdio.h>

void swap(int *arr,int sz)
{
	int i = 0;
	int j = sz-1;
	int m = 0;
	while (i != j)
	{
		if (arr[i] % 2 == 0)
		{
			if (arr[j] % 2 != 0)
			{
				m = arr[i];
				arr[i] = arr[j];
				arr[j] = m;
			}
			else
				j--;
		}
		else
			i++;
	}
	for (i = 0; i < sz; i++)
	{
		printf("%d ", arr[i]);
	}
	printf("\n");
}

int main()
{
	int arr[] = { 1, 2, 3, 4, 5, 6 };
	int sz = sizeof(arr) / sizeof(arr[0]);
	swap(arr,sz);
	return 0;
}